int main3()
/*
 Знайти середнє арифметичне всіх цілих чисел від 1 до 1000.
*/
{
    int res;  // переменная для хранения результата
    int sum;  // переменная для суммы всех чисел

    sum = 0;
    for (int i = 1; i <= 1000; i++)  // цикл, вычисление всех числ от 1 до 1000
    {
        sum += i;
    }
    res = sum / 1000;                // формула для среднее арифметическое
    std::cout << "The arithmetic mean of 1 to 1000 is " << res << '.' << std::endl;


    return 0;
}


