int main5()
/*
Написати програму, яка виводить на екран
таблицю множення на k, де k — номер варіанта. 
Наприклад, для 7-го варіанта:
7 x 2 = 14;
7 x 3 = 21.
*/
{
    int k;   // переменная для варианта, вводит пользователь
    int res; // переменная для результата
    
    std::cout << "Enter k: ";
    std::cin >> k;

    
    for (int i = 1; i <= 9; ++i)    // цикл с счетчиком, ввели цикловую переменную i
    {                               // плюсуем к i +1, пока i не станем =9
        res = k*i;                  // результат = номер варианта * i
        
        std::cout << k << '*' << i << '=' << res << std::endl;
    }
   
    return 0;
}

