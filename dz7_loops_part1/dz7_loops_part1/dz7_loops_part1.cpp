int main1()
/*
Напишіть програму, яка обчислює суму цілих
чисел від а до 500 (значення a вводиться з клавіатури).
*/
{
    int number;  // переменная для числа, вводимое пользователем
    int sum;     // переменная для суммы
    
    std::cout << "Enter number: ";
    std::cin >> number;

    sum = 0;  // изначально сумме присваиваем 0
    for (int i = number; i <= 500; ++i)  // начало цикла, ввод аккумулируемого числа i
    {
        sum += i;                        // цикл, добавление меняющегося числа "i"
    }
    std::cout << "Sum = " << sum;

    return 0;
}

